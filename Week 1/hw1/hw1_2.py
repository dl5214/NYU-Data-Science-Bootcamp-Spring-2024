# Iterate through the following list of animals and print each one in all caps.
# animals=['tiger', 'elephant', 'monkey', 'zebra', 'panther']


def main():
    animals = ['tiger', 'elephant', 'monkey', 'zebra', 'panther']
    for animal in animals:
        print(animal.upper())


if __name__ == '__main__':
    main()